namespace Felli
{
    /// <summary>
    /// Enum to see if the piece is playable or not.
    /// </summary>
    internal enum Playable
    {
        /// <summary>
        /// Piece is playable.
        /// </summary>
        Playable = 0,

        /// <summary>
        /// Piece is non playable.
        /// </summary>
        NonPlayable = 1,
    }
}